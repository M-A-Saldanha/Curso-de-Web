// Exemplos de Unários.

let num1 = 1
let num2 = 2

num1++ // operador pós fixado.
console.log(num1)
--num1 // operador pré fixado.
console.log(num1)

// operadores pré fixos tem maior prioridade do que os pós fixos.

console.log(++num1 === num2--)
console.log(num1 === num2)

/*
 Priorizar fazer codigos limpos e simples,evitar complexidades que sejam difíceis de entender
 posteriormente.
*/